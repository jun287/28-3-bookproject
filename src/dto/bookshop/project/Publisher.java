package dto.bookshop.project;

public class Publisher {

	private int publischer_no;
	private String publischer_name;
	private String publischer_website;
	
	public int getPublischer_no() {
		return publischer_no;
	}
	public void setPublischer_no(int publischer_no) {
		this.publischer_no = publischer_no;
	}
	
	public String getPublischer_name() {
		return publischer_name;
	}
	public void setPublischer_name(String publischer_name) {
		this.publischer_name = publischer_name;
	}
	
	public String getPublischer_website() {
		return publischer_website;
	}
	public void setPublischer_website(String publischer_website) {
		this.publischer_website = publischer_website;
	}
}
